import json

def format_lyrics(data):
    """
    주어진 JSON 데이터에서 가사 정보를 파싱하여
    [{"start": "m:ss.xx", "text": ""}, ...] 형식으로 반환합니다.

    Args:
        data (dict): syncLyric 정보가 담긴 JSON 데이터.

    Returns:
        list: 포맷팅된 가사 리스트.
    """
    try:
        start_times = data['syncLyric']['startTimeIndex']['startTimeIndex']
        lyrics = data['syncLyric']['contents']['contents'][0]['text']['text']

        # 시작 시간과 가사를 묶어서 리스트 생성
        formatted_list = []
        for start_time_str, text in zip(start_times, lyrics):
            # 문자열 시간을 float으로 변환
            start_time_float = float(start_time_str)

            # 분, 초, 밀리초 계산
            minutes = int(start_time_float // 60)
            seconds = int(start_time_float % 60)
            hundredths = int((start_time_float - int(start_time_float)) * 100)

            # m:ss.xx 형식으로 포맷팅
            formatted_time = f"{minutes:01}:{seconds:02}.{hundredths:02}"
            
            formatted_list.append({"start": formatted_time, "text": text})

        return formatted_list

    except KeyError as e:
        print(f"Error: Missing key in data - {e}")
        return []

